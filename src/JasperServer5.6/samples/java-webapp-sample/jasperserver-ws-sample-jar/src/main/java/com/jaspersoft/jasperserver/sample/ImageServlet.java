package com.jaspersoft.jasperserver.sample;


import com.jaspersoft.jasperserver.irplugin.wsclient.FileContent;
import com.jaspersoft.jasperserver.sample.controller.WSServlet;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

/**
 * @author Giulio Toffoli
 */
public class ImageServlet extends WSServlet {

    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        doPost(request, response);
    }

    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String imageName = request.getParameter("key");

        FileContent fc = (FileContent)request.getSession().getAttribute(imageName);

        if (fc != null)
        {
            response.setContentType(fc.getMimeType());
            java.io.OutputStream os = response.getOutputStream();
            os.write(fc.getData());
        }
    }


}